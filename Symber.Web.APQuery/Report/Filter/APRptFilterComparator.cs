
namespace Symber.Web.Report
{

	/// <summary>
	/// APRptColumn filter comparator, associated with APRptFilterType.
	/// </summary>
	public enum APRptFilterComparator
	{

		/// <summary>
		/// Equals
		/// </summary>
		Equals,


		/// <summary>
		/// Not equal
		/// </summary>
		NotEqual,


		/// <summary>
		/// Greater than
		/// </summary>
		GreaterThan,


		/// <summary>
		/// Greater or equal
		/// </summary>
		GreaterOrEqual,


		/// <summary>
		/// Less than
		/// </summary>
		LessThan,


		/// <summary>
		/// Less or equal
		/// </summary>
		LessOrEqual,


		/// <summary>
		/// Starts with
		/// </summary>
		StartsWith,


		/// <summary>
		/// Contains
		/// </summary>
		Contains,


		/// <summary>
		/// Does not contain
		/// </summary>
		DoesNotContain,


		/// <summary>
		/// Between the begin and end values
		/// </summary>
		Between,


		/// <summary>
		/// Includes
		/// </summary>
		Includes,


		/// <summary>
		/// Excludes
		/// </summary>
		Excludes

	}

}
